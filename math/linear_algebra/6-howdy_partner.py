#!/usr/bin/env python3

"""
Concatenates two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Parameters:
    - arr1 (list of int/float): The first array.
    - arr2 (list of int/float): The second array.

    Returns:
    - A new list containing the concatenation of arr1 and arr2.
    """
    return arr1 + arr2
